function sum(num1,num2)
{
    let total = num1+ num2;
    //implicit
    return total;
}
let output =sum(45,34);
console.log(`sum ${output}`);

function difference(num1,num2)
{
    let total = num1 - num2;
   
    return total;
}
let output1 =difference(45,34);
console.log( `Difference ${output1}`);